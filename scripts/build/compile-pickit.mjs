#!/usr/bin/env node
// AOT NIP pickit compiler.
// Reads pickit.config.json, compiles via @blizzhackers/nip-compiler,
// emits scripts/generated/pickit-nip.ts with a pluggable createChecker()
// and an embedded source-line table for verbose rule logging.

import { Parser, Binder, Emitter, OutputFormat, DispatchStrategy, d2Aliases } from "@blizzhackers/nip-compiler"
import { readFileSync, writeFileSync, existsSync } from "node:fs"
import { dirname, join, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const HERE = dirname(fileURLToPath(import.meta.url))
const SCRIPTS_DIR = resolve(HERE, "..")
const AETHER_DIR = resolve(SCRIPTS_DIR, "..")
const PICKIT_DIR = resolve(AETHER_DIR, "kolbot/d2bs/kolbot/pickit")
const CONFIG_FILE = resolve(SCRIPTS_DIR, "pickit.config.json")
const OUT_FILE = resolve(SCRIPTS_DIR, "generated/pickit-nip.ts")

function die(msg) { console.error("[pickit] " + msg); process.exit(1) }

// ── Config ────────────────────────────────────────────────────────
if (!existsSync(CONFIG_FILE)) die(`missing ${CONFIG_FILE}`)
const config = JSON.parse(readFileSync(CONFIG_FILE, "utf8"))
const files = Array.isArray(config.files) ? config.files : []
if (files.length === 0) die(`no "files" configured in pickit.config.json`)

for (const name of files) {
  const p = join(PICKIT_DIR, name)
  if (!existsSync(p)) die(`missing .nip file: ${p}`)
}

console.log(`[pickit] compiling ${files.length} .nip file(s): ${files.join(", ")}`)

// ── Parse + bind ──────────────────────────────────────────────────
const parser = new Parser()
const binder = new Binder()
let hadErrors = false

const sourceByFile = new Map()
const parsedFiles = files.map(name => {
  const path = join(PICKIT_DIR, name)
  const src = readFileSync(path, "utf8")
  sourceByFile.set(name, src.split(/\r?\n/))
  const file = parser.parseFile(src, name)
  const { diagnostics } = binder.bindFile(file)
  for (const d of diagnostics) {
    if (d.severity === "error") {
      hadErrors = true
      console.error(`[pickit] ${name}:${d.line}: ${d.message}`)
    }
  }
  return file
})
if (hadErrors) die("aborting due to bind errors")

// ── Emit IIFE checker ─────────────────────────────────────────────
const emitter = new Emitter({
  aliases: d2Aliases,
  outputFormat: OutputFormat.IIFE,
  dispatchStrategy: DispatchStrategy.Switch,
  prettyPrint: false,
  minify: true,
  includeSourceComments: false,
})
const iife = emitter.emit(parsedFiles).trim()

// ── Build rule-text table ─────────────────────────────────────────
// Emit every non-blank, non-comment line so verbose lookups (file, line)
// resolve to the original rule text. Keeps table small by skipping
// comment-only lines.
const ruleMap = {}
let ruleCount = 0
for (const [name, lines] of sourceByFile) {
  const bucket = {}
  for (let i = 0; i < lines.length; i++) {
    const raw = lines[i] ?? ""
    const trimmed = raw.trim()
    if (!trimmed || trimmed.startsWith("//")) continue
    bucket[i + 1] = trimmed
    ruleCount++
  }
  ruleMap[name] = bucket
}

// ── Emit .ts ──────────────────────────────────────────────────────
const out = `// GENERATED by scripts/build/compile-pickit.mjs — do not edit.
// Source .nip files: ${files.join(", ")}
// Compiler: @blizzhackers/nip-compiler
// @ts-nocheck

export type NipResult = 0 | 1 | -1
export interface NipItem {
  classid: number
  quality: number
  itemType: number
  getStatEx(stat: number, sub?: number): number
  getFlag(bit: number): boolean
}
export interface NipHelpers {
  checkQuantityOwned?: (classid: number, max: number) => boolean
  me?: { charlvl?: number; classid?: number; [k: string]: unknown }
  getBaseStat?: (classid: number, stat: string | number) => number
}
export interface NipVerboseResult {
  result: NipResult
  file: string | null
  line: number
}
export interface NipChecker {
  checkItem(item: NipItem): NipResult
  checkItem(item: NipItem, verbose: true): NipVerboseResult
  getTier(item: NipItem): number
  getMercTier(item: NipItem): number
}

const _factory = ${iife}

export function createChecker(helpers: NipHelpers = {}): NipChecker {
  return _factory(helpers) as NipChecker
}

export const PICKIT_FILES: readonly string[] = ${JSON.stringify(files)}

// file -> line -> rule text (for verbose logging of matched rule)
const RULES: Record<string, Record<number, string>> = ${JSON.stringify(ruleMap)}

/** Look up the source text of a matched rule. Returns null if unknown. */
export function describeRule(file: string | null, line: number): string | null {
  if (!file || !line) return null
  const bucket = RULES[file]
  return bucket ? bucket[line] ?? null : null
}
`
writeFileSync(OUT_FILE, out, "utf8")
console.log(`[pickit] wrote ${OUT_FILE}`)
console.log(`[pickit]   iife:  ${(iife.length / 1024).toFixed(1)}kb`)
console.log(`[pickit]   rules: ${ruleCount} source lines indexed`)
